//variables
import 'dart:ffi';

void main() {
  //variable can declare with var,val,const,final
  const a = "aditya";
  print(a);
  //variable can also can define in a way of datatype variable name
  int a1 = 21;
  print(a1);
  //null safety
  String? a2;
  print(a2);
  //late variable
  late String name;
  name = "aditya";
  print(name);
}
