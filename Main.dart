//Dart looping
void main() {
  //for loop
  for (int i = 0; i < 10; i++) {
    print("hello");
  }

  //while
  int i = 0;
  while (i < 9) {
    print("hello adi");
    i++;
  }

  //do-while
  var i1 = 2;
  do {
    print("jo jo");
    i1++;
  } while (i1 < -1);

  //iterate over list
  List a1 = ['a', 'b', 'c', 'd'];
  for (var c in a1) {
    print(c);
  }
}
