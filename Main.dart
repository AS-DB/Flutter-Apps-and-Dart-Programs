//Dart Numbers
void main() {
  //without mentioning the data type we can use dynamic data type
  dynamic a1 = "aditya";
  print(a1.length);

  //String to Number according to their data type
  var number2 = 21;
  var b = number2.toDouble();
  print(b);

  //round
  double k = 21.333;
  print(k.round());
}
